import { toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";

export function notifyInfo(message: string) {
  toast.info(message, {
    position: "bottom-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
  });
}

